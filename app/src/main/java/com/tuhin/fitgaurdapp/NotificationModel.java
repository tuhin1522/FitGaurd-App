package com.tuhin.fitgaurdapp;

public class NotificationModel {
    private int id;
    private String title, date, time;

    public NotificationModel(int id, String title, String date, String time) {
        this.id = id;
        this.title = title;
        this.date = date;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getDate() {
        return date;
    }

    public String getTime() {
        return time;
    }
}
